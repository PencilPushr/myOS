//=============================================================================
// Draw a character at pixel position
//=============================================================================

static void draw_char(uint32_t px, uint32_t py, char c, uint32_t fg, uint32_t bg) {
    if (!g_fb || c < 0 || c > 127) return;
    
    const uint8_t *glyph = font_8x8[(int)c];
    uint32_t *pixels = (uint32_t *)g_fb->base;
    uint32_t ppsl = g_fb->pitch / 4;
    
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            uint32_t x = px + col;
            uint32_t y = py + row;
            if (x < g_fb->width && y < g_fb->height) {
                pixels[y * ppsl + x] = color;
            }
        }
    }
}

//=============================================================================
// Simple Console State
//=============================================================================

static uint32_t cursor_x = 0;
static uint32_t cursor_y = 0;
static uint32_t console_fg = 0x00FFFFFF;
static uint32_t console_bg = 0x00102040;
#define CONSOLE_START_X 100
#define CONSOLE_START_Y 350
#define CONSOLE_COLS 60
#define CONSOLE_ROWS 10

static void console_putchar(char c) {
    if (c == '\n') {
        cursor_x = 0;
        cursor_y++;
    } else if (c == '\b') {
        if (cursor_x > 0) {
            cursor_x--;
            draw_char(CONSOLE_START_X + cursor_x * 8, CONSOLE_START_Y + cursor_y * 10, 
                     ' ', console_fg, console_bg);
        }
    } else if (c >= 32 && c < 127) {
        draw_char(CONSOLE_START_X + cursor_x * 8, CONSOLE_START_Y + cursor_y * 10, 
                 c, console_fg, console_bg);
        cursor_x++;
    }
    
    // Line wrap
    if (cursor_x >= CONSOLE_COLS) {
        cursor_x = 0;
        cursor_y++;
    }
    
    // Scroll (simple: just wrap to top)
    if (cursor_y >= CONSOLE_ROWS) {
        cursor_y = 0;
        // Clear line
        draw_rect(g_fb, CONSOLE_START_X, CONSOLE_START_Y, 
                 CONSOLE_COLS * 8, 10, console_bg);
    }
}

static void console_print(const char *s) {
    while (*s) {
        console_putchar(*s++);
    }
}

//=============================================================================
// Timer Interrupt Handler (IRQ0 = INT 32)
//=============================================================================

static void timer_handler(struct InterruptFrame *frame) {
    (void)frame;
    tick_count++;
    
    // Cycle through colors every 18 ticks (~1 second)
    uint32_t color;
    switch ((tick_count / 18) % 4) {
        case 0: color = 0x00FF0000; break;
        case 1: color = 0x0000FF00; break;
        case 2: color = 0x000000FF; break;
        case 3: color = 0x00FFFFFF; break;
        default: color = 0x00FFFFFF;
    }
    
    // Timer indicator in top-left
    if (g_fb) {
        draw_rect(g_fb, 30, 30, 40, 40, color);
    }
}

//=============================================================================
// Kernel Entry Point
//=============================================================================

__attribute__((section(".text.kernel_main")))
void kernel_main(struct BootInfo *boot_info) {
    struct FramebufferInfo *fb = &boot_info->framebuffer;
    g_fb = fb;
    
    // Dark blue background
    fill_screen(fb, 0x00102040);
    
    // White border
    uint32_t border = 20;
    uint32_t white = 0x00FFFFFF;
    draw_rect(fb, border, border, fb->width - 2*border, 4, white);
    draw_rect(fb, border, fb->height - border - 4, fb->width - 2*border, 4, white);
    draw_rect(fb, border, border, 4, fb->height - 2*border, white);
    draw_rect(fb, fb->width - border - 4, border, 4, fb->height - 2*border, white);
    
    uint32_t cx = fb->width / 2;
    uint32_t cy = 150;  // Moved up to make room for console
    
    // Step 1: GDT
    gdt_init();
    draw_rect(fb, cx - 175, cy - 30, 50, 60, 0x00FFFF00);
    
    // Step 2: PIC
    pic_init();
    draw_rect(fb, cx - 115, cy - 30, 50, 60, 0x00FF00FF);
    
    // Step 3: IDT
    idt_init();
    draw_rect(fb, cx - 55, cy - 30, 50, 60, 0x0000FFFF);
    
    // Step 4: Exception handlers
    exceptions_init();
    draw_rect(fb, cx + 5, cy - 30, 50, 60, 0x00FF8800);
    
    // Step 5: Timer
    idt_set_handler(32, timer_handler);
    pic_unmask_irq(0);
    draw_rect(fb, cx + 65, cy - 30, 50, 60, 0x008888FF);
    
    // Step 6: Keyboard
    keyboard_init();
    draw_rect(fb, cx + 125, cy - 30, 50, 60, 0x0088FF88);
    
    // Step 7: Enable interrupts
    __asm__ volatile("sti");
    draw_rect(fb, cx + 185, cy - 30, 50, 60, 0x0000FF00);
    
    // Draw labels
    const char *labels[] = {"GDT", "PIC", "IDT", "EXC", "TMR", "KBD", "STI"};
    uint32_t lx = cx - 175;
    for (int i = 0; i < 7; i++) {
        const char *l = labels[i];
        for (int j = 0; l[j]; j++) {
            draw_char(lx + 10 + j * 8, cy + 40, l[j], 0x00FFFFFF, 0x00102040);
        }
        lx += 60;
    }
    
    // Draw memory bars
    uint32_t bar_x = 50;
    uint32_t bar_y = fb->height - 60;
    uint32_t bar_count = 0;
    for (uint32_t i = 0; i < boot_info->memory_map_count && bar_count < 30; i++) {
        if (boot_info->memory_map[i].type == MEMORY_TYPE_USABLE) {
            draw_rect(fb, bar_x + bar_count * 12, bar_y, 10, 30, 0x0000FF00);
            bar_count++;
        }
    }
    
    // Print welcome message
    console_print("MyOS Phase 2 Complete!\n");
    console_print("Type something: ");
    
    // Main loop - echo keyboard input
    while (1) {
        if (keyboard_has_key()) {
            char c = keyboard_getchar_nonblock();
            if (c) {
                console_putchar(c);
            }
        }
        __asm__ volatile("hlt");
    }
}
